# 9. f-string samenvoegen
naam = "Ozgur"
leeftijd = 30
print(f"Hallo, ik ben {naam} en ik ben {leeftijd} jaar oud.")

# 10. Complex voorbeeld
naam = "Sanne"
leeftijd = 28
is_student = False
print(f"Naam: {naam}\nLeeftijd: {leeftijd}\nStudent: {is_student}")
