# ✅ Oplossingen – Les 2: Variabelen en Datatypes

# 1. Naam printen
naam = "Ozgur"
print(naam)

# 2. Leeftijd printen
leeftijd = 30
print(leeftijd)

# 3. Naam en leeftijd samen
print("Mijn naam is", naam, "en ik ben", leeftijd, "jaar oud.")

# 4. Lengte
lengte = 1.80
print("Mijn lengte is", lengte, "meter.")

# 5. Boolean
is_student = False
print("Ben ik student?", is_student)

# 6. Berekeningen
a = 10
b = 4
print("Som:", a + b)
print("Verschil:", a - b)
print("Product:", a * b)
print("Quotiënt:", a / b)

# 7. Type tonen
print(type(naam))
print(type(leeftijd))
print(type(lengte))

# 8. Typeconversie
jaar = "25"
jaar = int(jaar)
print(jaar + 5)

# 9. f-string
print(f"Hallo, ik ben {naam} en ik ben {leeftijd} jaar oud.")

# 10. Mini-profiel
naam = "Ozgur"
leeftijd = 30
lengte = 1.80
is_student = False
print(f"Naam: {naam}")
print(f"Leeftijd: {leeftijd}")
print(f"Lengte: {lengte} m")
print(f"Student: {is_student}")
